import asyncio
from utils.zhipu_client import zhipu_complete_async
from utils.logger import logger


async def test_model_answer(question: str, context: str) -> str:
    """
    测试模型回答能力

    Args:
        question: 用户问题
        context: 上下文内容

    Returns:
        str: 模型生成的答案
    """
    try:
        system_prompt = (
            "你是一个专业的文档问答助手，能够基于提供的文档内容准确回答用户问题。"
            "请遵循以下原则：\n"
            "1. 只基于提供的文档内容回答问题\n"
            "2. 如果文档中没有相关信息，请明确说明\n"
            "3. 保持答案的准确性和完整性\n"
            "4. 使用清晰、易懂的语言"
        )

        user_prompt = (
            f"用户问题：{question}\n\n"
            f"相关文档内容：\n{context}\n\n"
            "请基于上述文档内容回答用户问题。如果文档中没有相关信息，请说明无法找到相关信息。"
        )

        answer = await zhipu_complete_async(
            prompt=user_prompt,
            system_prompt=system_prompt,
            temperature=0.3,
            max_tokens=32768,
        )

        return answer.strip() if answer else "模型回答失败"

    except Exception as e:
        logger.error(f"模型回答测试失败: {str(e)}")
        return f"测试失败: {str(e)}"


async def interactive_test():
    """交互式测试模式"""
    print("=" * 60)
    print("模型回答能力测试工具")
    print("=" * 60)
    print("输入 'quit' 或 'exit' 退出")
    print("=" * 60)

    while True:
        try:
            # 获取用户输入
            print("\n请输入您的问题:")
            question = input("问题: ").strip()
            
            if question.lower() in ['quit', 'exit', '退出', 'q']:
                print("感谢使用，再见！")
                break
            
            if not question:
                print("请输入有效的问题。")
                continue

            print("\n请输入上下文内容:")
            context = input("上下文: ").strip()
            
            if not context:
                print("请输入有效的上下文内容。")
                continue

            print(f"\n正在生成答案...")
            
            # 调用模型生成答案
            answer = await test_model_answer(question, context)
            
            # 显示结果
            print("\n" + "=" * 60)
            print("模型回答:")
            print("-" * 60)
            print(answer)
            print("=" * 60)

        except KeyboardInterrupt:
            print("\n\n程序被用户中断，正在退出...")
            break
        except Exception as e:
            logger.error(f"测试过程中发生错误: {str(e)}")
            print(f"抱歉，处理时出现了错误: {str(e)}")


